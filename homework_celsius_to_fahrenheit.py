print("Este programa te facilita la conversion de Grados Celcius a Grados Fahrenheit")

grados_celcius = int(input("Dime el valor de los grados Celcius: "))

grados_fahrenheit = ((grados_celcius * 9/5) + 32)

print("{} grados Celsius equivalen a {} grados Fahrenheit.".format(grados_celcius, grados_fahrenheit))